; input:
;  a = PORTRAIT_* constant
;  b = portrait slot (0 or 1)
;  c = EMOTION_* constant
;  de = coordinates
DrawPortrait::
	push af
	push bc
	push de
	push hl
	push bc
	push de
	ld e, b
	ld d, c
	push de
	ld e, a
	ld d, 0
	ld bc, 10 ; size of each portrait entry
	call MultiplyBCByDE
	ld b, h
	ld c, l
	pop de
	ld hl, Portraits
	add hl, bc

	; tileset
	ld a, [hli]
	ld c, a
	ld a, [hli]
	ld b, a
	push hl
	farcall GetTilesetGfxPointer
	ld c, d
	call LoadPortraitTiles
	pop hl

	; palette
	ld a, [hli]
	ld c, a
	ld a, [hli]
	ld b, a
	push hl
	farcall GetPaletteGfxPointer
	call LoadPortraitPalettes
	pop hl

	; tilemap
	pop de
	pop bc
	ld a, b ; slot
	ld [wPortraitSlot], a
	ld a, c ; emotion
	ld [wPortraitEmotion], a
	ld b, $00
	sla c ; *2
	add hl, bc
	ld a, [hli]
	ld c, a
	ld a, [hli]
	ld b, a
	farcall GetTilemapGfxPointer
	ld a, [wPortraitEmotion]
	ld c, a
	ld a, [wPortraitSlot]
	farcall LoadPortraitAttributeMap
	pop hl
	pop de
	pop bc
	pop af
	ret

